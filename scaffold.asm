  ldi ZH, high(sine*2)

main:

  ldi r16, 0b0001_0100
  ldi r17, 0b0001_0000
  rcall pwm
  ldi r16, 0b0001_0100
  ldi r17, 0b0000_0100
  rcall pwm

  ldi r16, 0b0001_0010
  ldi r17, 0b0001_0000
  rcall pwm
  ldi r16, 0b0001_0010
  ldi r17, 0b0000_0010
  rcall pwm

  ldi r16, 0b0000_0110
  ldi r17, 0b0000_0010
  rcall pwm
  ldi r16, 0b0000_0110
  ldi r17, 0b0000_0100
  rcall pwm

  inc ZL

rjmp main



pwm:
  out DDRA, r16
  out PORTA, r17

  subi ZL, 43
  lpm r18, Z
  clr r19
pwm_1:
  inc r19
  rcall wait
  cpse r18,r19
  rjmp pwm_1

  clr r18
  out PORTA,r18
pwm_2:
  inc r19
  rcall wait
  cpse r18,r19
  rjmp pwm_2

  ret

wait:
  rjmp PC+1
  rjmp PC+1
  rjmp PC+1
  ret

